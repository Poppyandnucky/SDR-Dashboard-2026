/**
 * One-off script: fetch Kenya county GeoJSON and emit simplified SVG paths.
 * Run: node scripts/generate-kenya-map.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const BASE =
  "https://raw.githubusercontent.com/Mondieki/kenya-counties-subcounties/master/geojson";
const OUT = path.join(__dirname, "../lib/data/kenya-county-paths.ts");
const VIEW = { width: 800, height: 900, pad: 24 };

const listRes = await fetch(
  "https://api.github.com/repos/Mondieki/kenya-counties-subcounties/contents/geojson"
);
const files = (await listRes.json())
  .filter((f) => f.name.endsWith(".json"))
  .map((f) => f.name);

const counties = [];

for (const file of files) {
  const id = file.replace(/\.json$/, "");
  const geo = await fetch(`${BASE}/${file}`).then((r) => r.json());
  counties.push({ id, name: titleCase(id), geo });
}

function titleCase(slug) {
  return slug
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

function collectCoords(geo, out = []) {
  if (geo.type === "Feature") return collectCoords(geo.geometry, out);
  if (geo.type === "FeatureCollection") {
    geo.features.forEach((f) => collectCoords(f, out));
    return out;
  }
  if (geo.type === "GeometryCollection") {
    geo.geometries.forEach((g) => collectCoords(g, out));
    return out;
  }
  if (geo.type === "Polygon") {
    geo.coordinates.forEach((ring) => ring.forEach(([lng, lat]) => out.push([lng, lat])));
  } else if (geo.type === "MultiPolygon") {
    geo.coordinates.forEach((poly) =>
      poly.forEach((ring) => ring.forEach(([lng, lat]) => out.push([lng, lat])))
    );
  }
  return out;
}

const allCoords = counties.flatMap((c) => collectCoords(c.geo));
const minLng = Math.min(...allCoords.map((c) => c[0]));
const maxLng = Math.max(...allCoords.map((c) => c[0]));
const minLat = Math.min(...allCoords.map((c) => c[1]));
const maxLat = Math.max(...allCoords.map((c) => c[1]));

const innerW = VIEW.width - VIEW.pad * 2;
const innerH = VIEW.height - VIEW.pad * 2;

function project(lng, lat) {
  const x = VIEW.pad + ((lng - minLng) / (maxLng - minLng)) * innerW;
  const y = VIEW.pad + ((maxLat - lat) / (maxLat - minLat)) * innerH;
  return [x, y];
}

function simplifyRing(points, minDist = 2.5) {
  if (points.length <= 2) return points;
  const out = [points[0]];
  for (let i = 1; i < points.length; i++) {
    const [x1, y1] = out[out.length - 1];
    const [x2, y2] = points[i];
    const d = Math.hypot(x2 - x1, y2 - y1);
    if (d >= minDist) out.push(points[i]);
  }
  const last = points[points.length - 1];
  const [lx, ly] = out[out.length - 1];
  if (Math.hypot(last[0] - lx, last[1] - ly) > 0.5) out.push(last);
  return out;
}

function ringToPath(ring) {
  const pts = simplifyRing(ring.map(([lng, lat]) => project(lng, lat)));
  if (!pts.length) return "";
  return (
    pts.map((p, i) => `${i === 0 ? "M" : "L"}${p[0].toFixed(1)},${p[1].toFixed(1)}`).join(" ") +
    " Z"
  );
}

function geometryToPath(geometry) {
  if (geometry.type === "Polygon") {
    return geometry.coordinates.map(ringToPath).join(" ");
  }
  if (geometry.type === "MultiPolygon") {
    return geometry.coordinates.flatMap((poly) => poly.map(ringToPath)).join(" ");
  }
  if (geometry.type === "GeometryCollection") {
    return geometry.geometries.map(geometryToPath).join(" ");
  }
  return "";
}

function geoToPath(geo) {
  if (geo.type === "Feature") return geometryToPath(geo.geometry);
  if (geo.type === "FeatureCollection") return geo.features.map((f) => geometryToPath(f.geometry)).join(" ");
  return geometryToPath(geo);
}

const kakamega = counties.find((c) => c.id === "kakamega");
let kakCentroid = [400, 450];
if (kakamega) {
  const coords = collectCoords(kakamega.geo);
  const avgLng = coords.reduce((s, c) => s + c[0], 0) / coords.length;
  const avgLat = coords.reduce((s, c) => s + c[1], 0) / coords.length;
  kakCentroid = project(avgLng, avgLat);
}

const entries = counties
  .map((c) => {
    const d = geoToPath(c.geo);
    return `  { id: "${c.id}", name: "${c.name}", d: ${JSON.stringify(d)} }`;
  })
  .join(",\n");

const ts = `/** Auto-generated by scripts/generate-kenya-map.mjs — do not edit by hand. */
export const KENYA_MAP_VIEWBOX = "0 0 ${VIEW.width} ${VIEW.height}";

export interface KenyaCountyPath {
  id: string;
  name: string;
  d: string;
}

export const KENYA_COUNTY_PATHS: KenyaCountyPath[] = [
${entries}
];

/** Projected centroid for Kakamega label callout (viewBox coords). */
export const KAKAMEGA_MAP_POINT = { x: ${kakCentroid[0].toFixed(1)}, y: ${kakCentroid[1].toFixed(1)} };
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, ts);
console.log(`Wrote ${counties.length} counties to ${OUT}`);
